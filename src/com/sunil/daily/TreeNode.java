package com.sunil.daily;

public class TreeNode {
    String value;
    TreeNode left;
    TreeNode right;

    public TreeNode(String value, TreeNode left, TreeNode right) {
        this.value = value;
        this.left = left;
        this.right = right;
    }

    public TreeNode(String value) {
        this.value = value;
    }
}
